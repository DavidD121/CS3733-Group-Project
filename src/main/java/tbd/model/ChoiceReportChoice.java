package tbd.model;

public class ChoiceReportChoice {
	public final String name;
	public final String uuid;
	public final String dateCreated;
	public final String dateCompleted;
	
	public ChoiceReportChoice(String n, String u, String dCr, String dCom) {
		name = n;
		uuid = u;
		dateCreated = dCr;
		dateCompleted = dCom;
	}

}